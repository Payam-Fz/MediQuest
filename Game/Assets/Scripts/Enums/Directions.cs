{
    // Ordered in counter-clockwise rotation starting from front-facing
    Front = 0,
    RightFront = 1,
    Right = 2,
    RightBack = 3,
    Back = 4,
    LeftBack = 5,
    Left = 6,
    LeftFront = 7,
    Standing = 10
}
